package model;

public class ColourScheme {
	private String id, name, dark_colour, med_colour, light_colour, text_colour;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDark_colour() {
		return dark_colour;
	}

	public void setDark_colour(String dark_colour) {
		this.dark_colour = dark_colour;
	}

	public String getMed_colour() {
		return med_colour;
	}

	public void setMed_colour(String med_colour) {
		this.med_colour = med_colour;
	}

	public String getLight_colour() {
		return light_colour;
	}

	public void setLight_colour(String light_colour) {
		this.light_colour = light_colour;
	}

	public String getText_colour() {
		return text_colour;
	}

	public void setText_colour(String text_colour) {
		this.text_colour = text_colour;
	}
	
}
